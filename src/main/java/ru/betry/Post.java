package ru.betry;

class Post {

    private String title;
    private String photo;
    private String geo;


    public String getTitle() {
        return title;
    }

    public String getPhoto() {
        return photo;
    }

    public String getGeo() {
        return geo;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public void setGeo(String geo) {
        this.geo = geo;
    }
}
